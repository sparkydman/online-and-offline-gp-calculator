   public void fill_tables(String level,int sem, JTable table, JLabel label){
       DecimalFormat df = new DecimalFormat("#.###");
       double total_credit = 0;double gp1;
       double total_grade = 0; 
       int getrow = jTable1.getSelectedRow();
      
       String regnum = jTable1.getModel().getValueAt(getrow, 3).toString();
       try{
           String qle ="select courseTittle as 'Course Tittle',courseCode as 'Course Code',creditLoad as 'Credit Load',score as 'Score'"
                   + ",grade as 'Grade',grade_unit as 'Grade Point' from course where regno = '"+regnum+"'  and level ='"+level+"' and semester"
                   + "='"+sem+"' ";
           pst = conn.prepareStatement(qle);
           rs = pst.executeQuery();
           table.setModel(DbUtils.resultSetToTableModel(rs));
           
           int row = table.getRowCount();
           for(int r = 0; r < row; r++){
               total_credit = total_credit + (int)table.getModel().getValueAt(r, 2);
               total_grade = total_grade + (int)table.getModel().getValueAt(r, 5);
           }
          
          
             gp1 = total_grade / total_credit ;
          label.setText("This Semester GP: "+ df.format(gp1));
           
       }catch(Exception e){
           JOptionPane.showMessageDialog(null,e);
       }
   }
   
