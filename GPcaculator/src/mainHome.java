  public static String  grade_level( double cgpa){
 String gradeLevel;
 if(cgpa >= 4.50){
  gradeLevel = "FIRST CLASS";
 }else if (cgpa >= 3.50) {
  gradeLevel = "SECOND CLASS UPPER";
 }else if (cgpa >= 2.50) {
  gradeLevel = "SECOND CLASS LOWER";
 }else if (cgpa >= 1.50) {
  gradeLevel = "THIRD CLASS";
 }else{
  gradeLevel = "PASS";
 }
 return gradeLevel;
         }
  
  public static char   calculate_grade(int grade){
      char gradeValue ;
      if(grade >= 70){
        gradeValue = 'A';
      }else if(grade >= 60){
        gradeValue = 'B';
      }else if(grade >= 50){
        gradeValue = 'C';
      }else if(grade >= 45){
        gradeValue = 'D';
      }else {
        gradeValue = 'F';
      }
      return gradeValue;
     }
  
  public static int  getgrade_point( char grade){
      int gradeValue;
      switch(grade){
        case 'A':
        gradeValue = 5;
        break;
        case 'B':
        gradeValue = 4;
        break;
        case 'C':
        gradeValue = 3;
        break;
        case 'D':
        gradeValue = 2;
        break;
        default:
        gradeValue = 0;
        break;
      }
      return gradeValue;
     }
  
